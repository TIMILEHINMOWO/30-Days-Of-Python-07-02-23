# Calculating the area of a triangle

height = int(input("Enter height: "))
base = int(input("Enter base: "))
area = 0.5*base*height
print("Area of triangle is", area)

# Calculating the perimeter of a triangle

side_a = int(input("Enter side a: "))
side_b = int(input("Enter side b: "))
side_c = int(input("Enter side c: "))
perimeter_tri = side_a + side_b + side_c
print("Perimeter of triangle is", perimeter_tri)

# Preimeter of a rectangle

length = int(input("Enter length: "))
width = int(input("Enter width: "))
perimeter_rect = 2*length + 2*width
print("Perimeter of rectangle is", perimeter_rect)

# Area and circumference of circle

radius = int(input("Enter radius: "))
pi = 3.14
area = pi*radius**2
circumference = 2*pi*radius
print("Area of circle is",area)
print("Area of circumference is",circumference)















